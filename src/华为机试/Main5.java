    private static int cal(int N){
    	int [] dp = new int[N+1];
        if(N==0) return 0;
        if(N==1) return 1;
        if(N==2) return 2;
        dp[0]=0;
        dp[1]=1;
        dp[2]=2;
        for(int i=3;i<=N;i++){
            dp[i] = dp[i/2]+1;
        }
        return dp[N];
    }
